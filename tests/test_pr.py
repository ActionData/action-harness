"""Tests for PR creation via gh CLI. Mocks subprocess.run."""

from pathlib import Path
from unittest.mock import MagicMock, patch

from action_harness.models import EvalResult, PrResult, WorkerResult
from action_harness.pr import _build_pr_body, _read_proposal_why, create_pr


def _success_eval() -> EvalResult:
    return EvalResult(
        success=True,
        stage="eval",
        commands_run=4,
        commands_passed=4,
    )


def _failed_eval() -> EvalResult:
    return EvalResult(
        success=False,
        stage="eval",
        error="Eval failed: uv run pytest -v",
        commands_run=2,
        commands_passed=1,
        failed_command="uv run pytest -v",
    )


def _sample_worker() -> WorkerResult:
    return WorkerResult(
        success=True,
        stage="worker",
        cost_usd=0.42,
        duration_seconds=120.0,
        worker_output="All tests pass, feature implemented.",
    )


class TestReadProposalWhy:
    def test_extracts_why_section(self, tmp_path: Path) -> None:
        change_dir = tmp_path / "openspec" / "changes" / "my-change"
        change_dir.mkdir(parents=True)
