    def test_no_background_when_no_proposal(self, tmp_path: Path) -> None:
        mock = self._mock_git_calls()
        with patch("action_harness.pr.subprocess.run", mock):
            body = _build_pr_body("my-change", _success_eval(), tmp_path, "main")
