class TestBuildPrBody:
    """Test _build_pr_body with enriched sections."""

    def _mock_git_calls(
        self,
        diff_stat: str | None = "file.py | 10 ++++\n 1 file changed",
        commit_log: str | None = "abc1234 Add feature",
    ) -> MagicMock:
        mock = MagicMock()

        def side_effect(cmd: list[str], **kwargs: object) -> MagicMock:
            result = MagicMock()
            if "diff" in cmd and "--stat" in cmd:
                if diff_stat is None:
                    result.returncode = 1
                    result.stdout = ""
                else:
                    result.returncode = 0
                    result.stdout = diff_stat
            elif "log" in cmd and "--oneline" in cmd:
                if commit_log is None:
                    result.returncode = 1
                    result.stdout = ""
                else:
                    result.returncode = 0
                    result.stdout = commit_log
            else:
                result.returncode = 0
                result.stdout = ""
            result.stderr = ""
            return result

        mock.side_effect = side_effect
        return mock

    def test_includes_change_name(self, tmp_path: Path) -> None:
        mock = self._mock_git_calls()
        with patch("action_harness.pr.subprocess.run", mock):
            body = _build_pr_body("add-logging", _success_eval(), tmp_path, "main")
        assert "add-logging" in body

    def test_includes_eval_summary_success(self, tmp_path: Path) -> None:
        mock = self._mock_git_calls()
        with patch("action_harness.pr.subprocess.run", mock):
            body = _build_pr_body("test", _success_eval(), tmp_path, "main")
        assert "4/4 eval commands passed" in body

    def test_includes_eval_summary_failure(self, tmp_path: Path) -> None:
        mock = self._mock_git_calls()
        with patch("action_harness.pr.subprocess.run", mock):
            body = _build_pr_body("test", _failed_eval(), tmp_path, "main")
        assert "1/2 eval commands passed" in body
        assert "uv run pytest -v" in body

    def test_includes_footer(self, tmp_path: Path) -> None:
        mock = self._mock_git_calls()
        with patch("action_harness.pr.subprocess.run", mock):
            body = _build_pr_body("test", _success_eval(), tmp_path, "main")
